<h1>Inception & Raise
of V.G.R</h1>
<p>V.G.R started its historic journey in 1986 with the inception of a Girls Junior College at Vijayawada. And since then there has been no turning back. The inception of V.G.R Educational Institutions was the result of a vision rather than the mere setting up of an institution. Our Founders and Mentors, Dr. B. S. Rao garu and Dr. Jhansi Lakshmi Bai garu, were hugely successful in the medical profession and decided to look at a fresh approach to Intermediate level education. A systematised methodology was introduced that changed the very face of education in Andhra Pradesh, for the better. A fresh chapter in the Pre-university stream of education in the state began with the introduction of a more professional approach to teaching and staff management.</p>

<p>The growth of V.G.R - post 1986, has been phenomenal and part of folk-lore. A Boys Junior college(1991), branches in Hyderabad (1996), Visakhapatnam (1998), Tirupathi and Guntur (2000), Eluru, Bhimavaram, Rajahmundhry, Kakinada and Machilipatnam (2002), Tenali, Ongloe, xkakulam, Amalapuram, Chittoor, Nellore and Ananthapur (2003) in Andhra Pradesh.</p>

<p>V.G.R continued its march forward by stepping beyond the state. Branches were opened in Delhi (2004), Hamirpur-Himachal Pradesh (2005) and Bellary - Karnataka. In 2006, the institution opened IIT-JEE / AIEEE / PMT Coaching Centres at Mandi-Himachal Pradesh, Ranchi and Bokaro - Jarkhand and Indore Madhya Pradesh. The Group continued its march forward with the inception of V.G.R Techno Schools in various districts of AP in 2008, and expanding to cover fourteen districts of the state and 3 districts of Karnataka including four branches in Bangalore by 2011.</p>
